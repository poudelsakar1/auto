const getCarMakeList = (): IDropdownItem[] =>
  _map(newSearchData, carMake => ({
    name: carMake.branch,
    value: carMake.branch,
  }))

const getCarModels = (selectedBranch: string): IDropdownItem[] => {
  const carMake: IBranch | undefined = _find(
    newSearchData,
    carMake => carMake.branch === selectedBranch,
  )

  if (typeof carMake !== 'undefined') {
    return _map(carMake.model, model => ({
      name: model.model,
      value: model.model,
    }))
  }

  return []
}

const getCarTrims = (
  selectedBranch: string,
  modelName: string,
): IDropdownItem[] => {
  const carMake: IBranch | undefined = _find(
    newSearchData,
    carMake => carMake.branch === selectedBranch,
  )

  if (typeof carMake !== 'undefined') {
    const carModel: any = _find(carMake.model, item => item.model === modelName)

    if (typeof carModel !== 'undefined') {
      return _map(carModel.trims, trim => ({
        name: trim,
        value: trim,
      }))
    }
  }

  return []
}

